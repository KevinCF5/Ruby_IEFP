# 1. Criação de dois símbolos
:erro 
:info

# 2. Mostrar os símbolos no ecrã
puts :erro
puts :info

# 3. Comentário:
# Um símbolo em Ruby é um identificador imutável e único, usado normalmente
# em vez de uma string quando se quer representar nomes, chaves de hash,
# ou valores constantes que não precisam de ser alterados.
# Exemplo: usar :nome como chave de hash é mais eficiente que "nome".